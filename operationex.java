public class operationex
{
	public static void main(String args[])
	{
		int a=6;
		System.out.println("a is : "+a);
		a+=8;
		System.out.println("a is : "+a);
		a-=4;
		System.out.println("a is : "+a);
		a*=5;
		System.out.println("a is : "+a);
		a/=10;
		System.out.println("a is : "+a);
		a%=3;
		System.out.println("a is : "+a);
		a++;
		System.out.println("a is : "+a);
		a--;
		System.out.println("a is : "+a);
	}
}
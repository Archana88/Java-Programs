public class longex
{
	public static void main(String args[])
	{
		long hb;
		hb=1245_8675L;
		System.out.println("The number is : "+hb);
		hb=1245_8675l;
		System.out.println("The number is : "+hb);
		hb=1245_8675;
		System.out.println("The number is : "+hb);
		hb=0x7fff_abcd;
		System.out.println("The hexadecimal number is : "+hb);
		
	}
}